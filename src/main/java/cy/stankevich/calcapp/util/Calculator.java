package cy.stankevich.calcapp.util;

public class Calculator {
    public static float sum(float num1, float num2, char operation) {
        return num1 + num2;
    }

    public static float deduct(float num1, float num2, char operation) {
        return num1 - num2;
    }

    public static float multyply(float num1, float num2, char operation) {
        return num1 * num2;
    }
}