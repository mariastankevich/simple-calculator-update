package cy.stankevich.calcapp.util;

import java.util.Scanner;

public class Calculator {

    public static int sum(int num1, int num2) {
        int resultsum = num1 + num2;
        return resultsum;
    }

    public static int deduct(int num1, int num2) {
        int resultdeduct = num1 - num2;
        return resultdeduct;
    }

    public static float multyply(float num1, float num2) {
        float resultmultyply = num1 * num2;
        return resultmultyply;
    }
}